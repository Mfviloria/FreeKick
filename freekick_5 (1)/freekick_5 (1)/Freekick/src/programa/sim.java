    private void animActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_animActionPerformed
        // TODO add your handling code here:
        try{
            ang = Double.parseDouble(ingang.getText());
            vo = Double.parseDouble(ingvo.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si hay un error
        }
        if (ang < 0 || vo < 0 ) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores positivos.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
        }
        ang = Math.toRadians(180-ang);
        tr = 0;
        
        int arcx1 = 220;
        int arcy1 = 110;
        int arcy2 = 420;
        int arcx2 = 300;
        int i;
        t = (2*vo*Math.sin(ang))/9.8;
        boolean f = resis.isSelected();
        
        if (f == true) {
            Thread animation = new Thread(new Runnable() {
                @Override
                public void run() {
                    x = 850; 
                    y = 360; 

                    
                    while (tr <= t) {
                        
                        double newx = 850 + 2*posxfric(tr); 
                        double newy = 360 - 2*posyfric(tr);
                        

                        SwingUtilities.invokeLater(new Runnable() {
                            @Override
                            public void run() {
                                pelota.setLocation((int)newx, (int)newy);
                            }
                        });
  
                        
                        double vy = vo*Math.sin(ang);
                        double vy2 = vy -9.8*tr;


                        if ((newx >= arcx1 && newx<=arcx2) && (newy >= arcy1 && newy <= arcy2)){
                            JOptionPane.showMessageDialog(null, "¡La pelota ha entrado al arco!");
                            pelota.setLocation(850,360);
                            break;
                        } 
                        if (newy > 360){
                            
                            pelota.setLocation(850,360);
                        
                            break;
                        }
                        if (newy < 0){
                            
                            pelota.setLocation(850,360);
                            
                            break;
                        }

                        
                        
                        if (vo > 0 & ang > 0) {
                        mt.addRow(new Object[]{df.format(tr), df.format(-posx(tr)),df.format(posy(tr)), df.format(vy2)});
                        }
                        
                        try {
                            // Pausa para hacer la animación más fluida
                            Thread.sleep(15); 
                        } catch (InterruptedException e) {
                            e.printStackTrace();
                        }
                        if (Et%2==0){
                            tr += 0.1; 
                        }
                        
                    }
                    pelota.setLocation(850,360);
                }
            });
            animation.start(); // Iniciar el hilo de animación
        }else{
            pelota.setLocation(850,360);
            Thread animation = new Thread(new Runnable() {
                @Override
                public void run() {
                    x = 850; 
                    y = 360; 
                    y2 = 0;

                    // Bucle para animar
                    while (y2 <=y) {
                        
                        
                        double newx = 850 + 2*posx(tr); 
                        double newy = 360 - 2*posy(tr);
                        y2 = newy;

                        SwingUtilities.invokeLater(new Runnable() {
                            @Override
                            public void run() {
                                pelota.setLocation((int)newx, (int)newy);
                            }
                        });
                        
                        double vy = vo*Math.sin(ang);
                        double vy2 = vy -9.8*tr;

                        // Detener la animación si la pelota toca o pasa el suelo
                        if ((newx >= arcx1 && newx<=arcx2) && (newy >= arcy1 && newy <= arcy2)){
                            JOptionPane.showMessageDialog(null, "¡La pelota ha entrado al arco!");
                            pelota.setLocation(850,360);
                            break;
                        }
                        
                        if (newy > 430){
                            
                            pelota.setLocation(850,360);
                            break;
                        }
                        if (newy < 0){
                            
                            pelota.setLocation(850,360);
                            break;
                        }
                        
                        
                        mt.addRow(new Object[]{df.format(tr), df.format(-posx(tr)),df.format(posy(tr)), df.format(vy2)});
                        
                        try {
                            // Pausa para hacer la animación más fluida
                            Thread.sleep(20); 
                        } catch (InterruptedException e) {
                            e.printStackTrace();
                        }
                        if (Et%2==0){
                            tr += 0.1; 
                        }
                        
                    }
                    pelota.setLocation(850,360);
                }
            });
            animation.start();
        }
        
    }//GEN-LAST:event_animActionPerformed

