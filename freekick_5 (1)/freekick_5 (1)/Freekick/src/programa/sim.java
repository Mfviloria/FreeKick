    public double posx(double t){
        
        return vo * Math.cos(ang) * t;
        
        
    }
    public double posy(double t) {
        double g = 9.8; 
        
        return (vo * t * Math.sin(ang)) - (0.5 * g * Math.pow(t, 2));
    }
    
    // Mejorar el uso de estas fórmulas.
    public double posxfric(double t) {
        double k = 0.5; // Coeficiente de fricción
        double vx = vo * Math.cos(ang);
        return vx*t - k*t*0.01;
        
    }
    public double posyfric(double t) {
        double g = 9.8;
        double k = 0.5;
        double vy = vo * Math.sin(ang) - 0.5*g*t*0.3;
        return vy *t - 0.5*g*Math.pow(t,2);
    }

